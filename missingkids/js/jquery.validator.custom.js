jQuery.validator.addMethod("date", function(dateString, element) {
  var date_regex = /^(0[1-9]|1[0-2])\/(0[1-9]|1\d|2\d|3[01])\/(19|20)\d{2}$/;
  return this.optional( element ) || (date_regex.test(dateString));
}, "Use mm-dd-yyyy for dates.");
